from matplotlib import cm
import numpy as np
import cv2


def resize_my(mask,tar_size=(512,512)):
    '''
    input img or mask ndarray [N,H,W]
    Note: the N can be 0
    return:
        resized img with shape [N,H2,W2]
    '''
    Nmask = mask.shape[0]
    data_type = mask.dtype
    if Nmask == 0:
        size = (0, tar_size[0], tar_size[1])
        mask2 = np.zeros((size), dtype=data_type)
    else:
        mask2 = cv2.resize(mask.transpose(1, 2, 0), tar_size).reshape(
            (tar_size[0], tar_size[1], Nmask)).transpose(2, 0, 1)
        mask2 = mask2.astype(data_type)
    return mask2

